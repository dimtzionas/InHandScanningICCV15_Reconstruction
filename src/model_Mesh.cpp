void Model::writeMesh_OFF___POSE_NULL( QString Filename )
{

     QFile myFileOUT( Filename );
     myFileOUT.open(QIODevice::WriteOnly);

     if(!myFileOUT.isOpen())
     {
         qDebug() << "writeMesh_OFF___POSE_NULL - Error, unable to open" << Filename << "for output";
         return;
     }

     QTextStream myStreamOUT(&myFileOUT);

     ////////////////////////////////////////////////////////////////////////////////////////////////
     ////////////////////////////////////////////////////////////////////////////////////////////////

     myStreamOUT << "OFF" << "\n" << totalVertices << " " << totalTriangles << " " << 0 << "\n";

     ////////////////////////////////////////////////////////////////////////////////////////////////
     ////////////////////////////////////////////////////////////////////////////////////////////////

     for (int vvv=0; vvv<totalVertices; vvv++)
     {
             myStreamOUT << mesh.vertices[vvv](0) << " ";
             myStreamOUT << mesh.vertices[vvv](1) << " ";
             myStreamOUT << mesh.vertices[vvv](2) << "\n";
     }

     ////////////////////////////////////////////////////////////////////////////////////////////////
     ////////////////////////////////////////////////////////////////////////////////////////////////

     for (int tr=0; tr<totalTriangles; tr++)
     {
             myStreamOUT << 3                             << " ";
             myStreamOUT << mesh.triangles[tr].vertexID_1 << " ";
             myStreamOUT << mesh.triangles[tr].vertexID_2 << " ";
             myStreamOUT << mesh.triangles[tr].vertexID_3 << "\n";
     }

     ////////////////////////////////////////////////////////////////////////////////////////////////
     ////////////////////////////////////////////////////////////////////////////////////////////////

     myFileOUT.close();

}



